r1<- subset(diabetic_data, race=="Caucasian")
r2<- subset(diabetic_data, race=="AfricanAmerican")
r3<- subset(diabetic_data, race=="Asian")
r4<- subset(diabetic_data, race=="Hispanic")
r5<- subset(diabetic_data, race=="Other")
x<- c(nrow(r1),nrow(r2),nrow(r3),nrow(r4),nrow(r5))
