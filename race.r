png(file="race_final.png")
pie(x,labels=piepercent,main="Analysis based on Race",col=rainbow(length(x)))
legend("topright",c("Caucasian","AfricanAmerican","Asian","Hispanic","Others"),cex=0.8,fill=rainbow(length(x)))
